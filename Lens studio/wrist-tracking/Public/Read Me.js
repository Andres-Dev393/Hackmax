/**
* Additional Information:
*
* This template allows you to track objects to the user's wrist.
* 
* To attach an object to the user's wrist, set the object as a child of 
* `Left Binding` and `Right Binding`. This template comes with 
* an example watch model that is tracked to the user's wrist under these objects. 
* 
* In addition to the watch model, there is also an occluder model. 
* This occluder model is used to occlude parts of your attachment that would be covered 
* by the wrist. Apply the `Occluder Left` and `Occluder Right` material on the model 
* that you want to use as an occluder. 
* 
* Happy creating!
**/